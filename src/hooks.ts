function registerMenus() {
  const menuIcon = `chrome://${addon.data.config.addonRef}/content/icons/favicon@0.5x.png`;
  ztoolkit.Menu.register("item", {
    tag: "menuitem",
    id: `${addon.data.config.addonRef}-menu-generate`,
    label: "LLM: Generate HTML and Open",
    commandListener: () => actionGenerateRichAndOpen(),
    icon: menuIcon,
  });
  ztoolkit.Menu.register("item", {
    tag: "menuitem",
    id: `${addon.data.config.addonRef}-menu-note`,
    label: "LLM: Save Plain Note",
    commandListener: () => actionSavePlainNote(),
    icon: menuIcon,
  });
}
